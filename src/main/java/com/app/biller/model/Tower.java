public class Tower {
	
	public int towerID;
	public String towerDesc;
	
	public int getTowerID() {
		return towerID;
	}
	public void setTowerID(int towerID) {
		this.towerID = towerID;
	}
	public String getTowerDesc() {
		return towerDesc;
	}
	public void setTowerDesc(String towerDesc) {
		this.towerDesc = towerDesc;
	}
	

}
